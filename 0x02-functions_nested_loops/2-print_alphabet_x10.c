#include "holberton.h"
/**
* print_alphabet_x10 - takes unspecificed number of arguments
* Description: 10 times the alphabet in lowercase
* Return: 0
*/
void print_alphabet_x10(void)
{
int num;
int il;

for (num = 0; num <= 9; num++)
{
for (il = 'i'; il <= 'i'; il++)
{
_putchar(il);
}
_putchar('\n');
}
}
